#!/bin/bash

FOLDER="devops_test"

# ПЕРВЫЙ ПРОХОД
echo "--- Шаг 1: Первая проверка ---"
if [ ! -d "$FOLDER" ]; then
  echo "Папки нет. Создаю..."
  mkdir "$FOLDER"
else
  echo "Папка уже тут."
fi

# ВТОРОЙ ПРОХОД (в том же скрипте!)
echo "--- Шаг 2: Вторая проверка (через 2 секунды) ---"
sleep 2 # подождем чуть-чуть для солидности

if [ ! -d "$FOLDER" ]; then
  echo "Папки всё еще нет? Что-то пошло не так."
else
  echo "Ага! Теперь скрипт видит папку и НЕ будет её создавать заново."
fi

ls -ld "$FOLDER"
